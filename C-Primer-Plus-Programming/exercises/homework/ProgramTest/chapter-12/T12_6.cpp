#include <iostream>

using namespace std;

template<typename T>
class Stack
{
public:
  Stack();
  bool empty() const;
  T peek() const;
  void push(T value);
  T pop();
  int getSize() const;
  void printStack() const;

private:
  T elements[100];
  int size;
};

template<typename T>
Stack<T>::Stack()
{
  size = 0;
}

template<typename T>
bool Stack<T>::empty() const
{
  return (size == 0);
}

template<typename T>
T Stack<T>::peek() const
{
  return elements[size - 1];
}

template<typename T>
void Stack<T>::push(T value)
{
  elements[size++] = value;
}

template<typename T>
T Stack<T>::pop()
{
  return elements[--size];
}

template<typename T>
int Stack<T>::getSize() const
{
  return size;
}

template <typename T>
void Stack<T>::printStack() const
{
  for (int i = 0; i < size; i++)
    cout << elements[i] << " ";
  cout << endl;
}

int main()
{
  Stack<int> intStack;
  Stack<double> doubleStack;

  for (int i = 0; i < 10; i++)
  {
    intStack.push(i);
    doubleStack.push(i * 1.0);
  }

  cout << "From intStack: ";
  intStack.printStack();
  cout << "From doubleStack: ";
  doubleStack.printStack();

  return 0;
}







